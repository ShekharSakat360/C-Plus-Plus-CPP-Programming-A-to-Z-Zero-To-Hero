#include <iostream>
using namespace std;

int main()
{
    int age = 24;
    string name = "Jack"; //Here we are telling the compiler to store collection of character
    char gender = 'M';//Here we are telling the compiler to store single character
    bool isMarried = false;//Here we are telling the compiler to store true value which is a boolean value
    float weight = 68.5;//Here we are telling the compiler to store decimal value

    cout << name  <<"\n";
    cout << age   << "\n";
    cout << gender <<"\n";
    cout << weight << "\n";
    cout << isMarried << "\n";

    return 0;
}