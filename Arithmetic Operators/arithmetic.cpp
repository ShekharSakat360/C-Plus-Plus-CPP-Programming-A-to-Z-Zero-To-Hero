#include <bits/stdc++.h>
using namespace std;

int main()
{
    cout << "Demonstration Of Arithmetic Operators " << endl;
    int x = 20;
    int y = 10;
 
    cout << (x + y) << endl; //Addition 
    cout << (x - y) << endl;//Subtraction
    cout << (x * y) << endl;//Multiplication
    cout << (x / y) << endl;//Division
    cout << (x % y) << endl;//Modular Division

    



    return 0;
}