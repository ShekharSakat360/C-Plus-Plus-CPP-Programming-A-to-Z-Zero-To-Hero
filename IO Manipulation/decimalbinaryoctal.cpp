#include<bits/stdc++.h>
using namespace std;

int main()
{
    int a = 26, b = 20;
    cout << "Demontration Of Printing Decimal Values in Hexadecimal,Octal and Binary " << endl;

    
    cout << a << " " << b << endl;

    cout << std::hex << endl;  //Using Hex flag to print values in hexadecimal format

    cout << a << " " << b << endl; 

    cout << std::oct << endl;  //Using oct flag to print values in hexadecimal format

    cout << a << " " << b << endl; 

    cout << std :: dec << endl;
    cout << a << " " << b << endl; 



    
    return 0;
}